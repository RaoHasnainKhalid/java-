package org.example;

public class Variable {
    public static void main(String[] args) {
        System.out.print("The Sum Of Number: ");
        byte a = 5;
        byte b = 7;
        byte c = 80;
        byte d = 100;
        int sum = a + b + c + d;
        System.out.println(sum);
        float f = 162.5f;
        double dbl = 5.7;
        char name = 'H';
        System.out.println(f + " "+ dbl  + " " + name);
    }
}
